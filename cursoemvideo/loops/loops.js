// ESTRUTURAS DE REPETIÇÃO (LOOPS)

var c = 1

// ESTRUTURA WHILE: ENQUANTO A CONDIÇÃO FOR VERDADEIRA, EXECUTE O BLOCO

while (c <= 10) {
    console.log(`${c}`)
    c++
}

// ESTRUTURA DO WHILE: EXECUTE O BLOCO ENQUANTO A CONDIÇÃO FOR VERDADEIRA

do {
    console.log(`${c}`)
    c++
} while (c <= 10)

// ESTRUTURA FOR

for(var c = 1;c <= 10;c++) {
    console.log(`${c}`)
}